void WriteComm(uint8_t byte)
{
    CS_SetLow();
    DC_SetLow();
    SPI1_Exchange8bit(byte);
    CS_SetHigh();
}

void WriteData(uint8_t byte)
{
    CS_SetLow();
    DC_SetHigh();
    SPI1_Exchange8bit(byte);
    CS_SetHigh();
}

void Screen_SPI_Init(){
    
    //Unlock commands
    WriteComm(0xFD);
    WriteComm(0x12); 
        
    //Display on
    WriteComm(0xAE); 
    
    //Set contrast
    WriteComm(0x81);
    WriteComm(0x0F);
    
    //Remap
    WriteComm(0xA0); 
    WriteComm(0x40);
    
    //Set clock
    WriteComm(0xB3); 
    WriteComm(0x00);
    
    //Set MUX ratio
    WriteComm(0xA8); 
    WriteComm(0xFF); 
    
    //Set offset
    WriteComm(0xA2);
    WriteComm(0x00);
    
    //Set Line
    WriteComm(0xA1); 
    WriteComm(0x00); 
    //0x5C,
    
    
    
    
    
    //Invert display
    //0xA7,
    
    //SetGPIO
    WriteComm(0xB5); 
    WriteComm(0x10);
    
    //Enable internal VDD regulator
    WriteComm(0xAB); 
    WriteComm(0x01); 
    

            
    //Set Gamma LUT
    WriteComm(0xB9); 

    
    
    //Set phase length
    WriteComm(0xB1);
    WriteComm(0x32); 
    
    //Set pre-charge voltage
    WriteComm(0xBC);
    WriteComm(0x17);
    
    //Set second pre-charge
    WriteComm(0xB6);
    WriteComm(0x01);
    
    //Set VCOMH Select level
    WriteComm(0xBE);
    WriteComm(0x05);
    
    //Voltage selections
    WriteComm(0xD5);
    WriteComm(0x60);
    
    //Reset display mode
    WriteComm(0xA4);
    
    
}

void BP1_CallBack(void)
{
    DC_SetLow();
    LATBbits.LATB12 = 0;
    
    Delay(300);
    
    /*Deactivate reset*/
    LATBbits.LATB12 = 1;
    //Set display off
    WriteComm(0xAE); 
    
    //Disable internal VDD regulator
    WriteComm(0xAB);
    WriteComm(0x00);
    
    LATAbits.LATA0 = 1;
    Delay(2000);
    LATAbits.LATA0 = 0;
    
    
}



